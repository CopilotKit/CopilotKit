import * as path from "node:path";
import type {
  CopilotKitProviderLocation,
  CopilotKitProps,
  ProviderChainEntry,
  UnserializableRef,
} from "../types";

export interface RenderEntryOptions {
  provider: CopilotKitProviderLocation;
  ancestors: ProviderChainEntry[];
  /** Specifier used to import the HooksAggregator. */
  aggregatorModule: string;
  /** Absolute directory where the generated entry.tsx will be written. */
  outDir: string;
  /** Overrides the user's runtimeUrl in the emitted <CopilotKitProvider>. */
  runtimeUrlOverride?: string;
}

/**
 * Emits the playground entry module. Imports every ancestor provider using
 * the real import source resolved from the user's file, plus CopilotKit(Provider)
 * itself from the user's package, and renders the reconstructed chain wrapping
 * HooksAggregator.
 *
 * Exports two components:
 * - `PlaygroundEntry` — the provider chain wrapping HooksAggregator only.
 *   Used by the MountedComponentsPanel to show user components in isolation.
 * - `ChatPlayground` — the same provider chain wrapping both HooksAggregator
 *   AND CopilotChat. Because both are resolved inside the same bundle module
 *   instance (forwarding-stubs → real v2), React context flows correctly and
 *   the chat connects to the spawned runtime.
 *
 * Ancestors with no resolvable import source (locally declared in the user's
 * file) are skipped with a comment — the wrapper is omitted rather than
 * generating broken code.
 */
export function renderEntry(opts: RenderEntryOptions): string {
  const { provider, ancestors, aggregatorModule, outDir } = opts;

  const lines: string[] = [
    "/* @generated by packages/vscode-extension/src/extension/playground/codegen. Do not edit. */",
    // The v2 chat components ship a precompiled Tailwind CSS bundle the
    // user is normally expected to import in their own app entry. Inside
    // the playground webview the user's code is mounted in isolation, so
    // we force-import the stylesheet here to guarantee the chat surface
    // renders with its real styling. The IIFE bundler's CSS collector
    // intercepts this and routes the contents into the bundle's `css`
    // payload (see iife-bundler.ts), which the webview's bundle-loader
    // then injects as a <style> element.
    'import "@copilotkit/react-core/dist/v2/index.css";',
    'import * as React from "react";',
    `import { HooksAggregator } from ${JSON.stringify(aggregatorModule)};`,
    `import { ${provider.importedName} } from ${JSON.stringify(
      provider.importSource,
    )};`,
    `import { CopilotChat } from "@copilotkit/react-core/v2";`,
  ];

  // Emit per-ancestor imports using the user's real import specifier.
  // Ancestors we can't resolve (locally declared, not exported) are skipped
  // with a comment — render falls through as if the wrapper was omitted.
  interface Renderable {
    ancestor: ProviderChainEntry;
    /** Local identifier used inside the generated JSX. */
    localName: string;
  }
  const renderables: Renderable[] = [];
  const skipped: ProviderChainEntry[] = [];
  const usedNames = new Set<string>([
    "HooksAggregator",
    "React",
    provider.importedName,
    "CopilotChat",
  ]);

  for (const a of ancestors) {
    // Lowercased tag names are HTML elements (<div>, <span>) — not providers.
    if (!/^[A-Z]/.test(a.tagName)) continue;
    if (a.importSource == null || a.importedName == null) {
      skipped.push(a);
      continue;
    }

    // Dedupe local name against imports already in scope.
    let local = a.tagName;
    let suffix = 2;
    while (usedNames.has(local)) {
      local = `${a.tagName}_${suffix++}`;
    }
    usedNames.add(local);

    const specifier = rewriteImportSpecifier(
      a.importSource,
      a.filePath,
      outDir,
    );
    if (a.isDefaultImport) {
      lines.push(`import ${local} from ${JSON.stringify(specifier)};`);
    } else if (a.importedName === a.tagName) {
      lines.push(
        `import { ${a.tagName}${
          local !== a.tagName ? ` as ${local}` : ""
        } } from ${JSON.stringify(specifier)};`,
      );
    } else {
      // User wrote `import { X as Y }` — reproduce the alias.
      lines.push(
        `import { ${a.importedName} as ${local} } from ${JSON.stringify(
          specifier,
        )};`,
      );
    }

    renderables.push({ ancestor: a, localName: local });
  }

  lines.push("");

  const playgroundEntry = renderProviderChain(
    opts,
    renderables,
    skipped,
    "<HooksAggregator />",
    "PlaygroundEntry",
  );

  const chatPlayground = renderProviderChain(
    opts,
    renderables,
    skipped,
    "<HooksAggregator />\n        <CopilotChat />",
    "ChatPlayground",
  );

  return (
    lines.join("\n") + "\n" + playgroundEntry + "\n" + chatPlayground + "\n"
  );
}

/**
 * Builds the JSX body for a single named export that wraps the given children
 * string inside the reconstructed provider chain.
 */
function renderProviderChain(
  opts: RenderEntryOptions,
  renderables: Array<{ ancestor: ProviderChainEntry; localName: string }>,
  skipped: ProviderChainEntry[],
  childrenJsx: string,
  exportName: string,
): string {
  const { provider } = opts;
  const fnLines: string[] = [];

  fnLines.push(`export function ${exportName}(): React.ReactElement {`);
  fnLines.push("  return (");

  const closeTags: string[] = [];
  let indent = "    ";

  for (const s of skipped) {
    fnLines.push(
      `${indent}{/* skipped ancestor: ${s.tagName} (locally declared in ${
        s.filePath
      }, not exported) */}`,
    );
  }

  for (const { ancestor, localName } of renderables) {
    const props = renderProps(ancestor.props);
    fnLines.push(`${indent}<${localName}${props}>`);
    closeTags.push(`</${localName}>`);
    indent += "  ";
  }

  const effectiveProps: CopilotKitProps = opts.runtimeUrlOverride
    ? { ...provider.props, runtimeUrl: opts.runtimeUrlOverride }
    : provider.props;
  const providerProps = renderProps(effectiveProps);
  fnLines.push(`${indent}<${provider.importedName}${providerProps}>`);
  indent += "  ";
  // Indent each line of the children block.
  for (const childLine of childrenJsx.split("\n")) {
    fnLines.push(`${indent}${childLine}`);
  }
  indent = indent.slice(2);
  fnLines.push(`${indent}</${provider.importedName}>`);

  for (let i = closeTags.length - 1; i >= 0; i--) {
    indent = indent.slice(2);
    fnLines.push(`${indent}${closeTags[i]}`);
  }

  fnLines.push("  );");
  fnLines.push("}");
  fnLines.push("");

  return fnLines.join("\n");
}

/**
 * Resolves the user's import specifier into something valid from outDir.
 * - Non-relative specifiers (npm packages, scoped packages) pass through.
 * - Relative specifiers are resolved against the importer's directory and
 *   re-expressed relative to outDir via toImportSpecifier.
 */
function rewriteImportSpecifier(
  userSpecifier: string,
  importerFilePath: string,
  outDir: string,
): string {
  if (!userSpecifier.startsWith(".")) return userSpecifier;
  const absolute = path.resolve(path.dirname(importerFilePath), userSpecifier);
  return toImportSpecifier(outDir, absolute);
}

function renderProps(props: CopilotKitProps): string {
  const parts: string[] = [];
  for (const [name, value] of Object.entries(props)) {
    if (name === "__spread") continue; // skip: spread captured as unserializable
    if (name === "children") continue; // JSX element wraps its own children
    parts.push(" " + renderProp(name, value));
  }
  return parts.join("");
}

function renderProp(
  name: string,
  value:
    | string
    | number
    | boolean
    | null
    | CopilotKitProps
    | CopilotKitProps[]
    | UnserializableRef,
): string {
  if (isUnserializable(value)) {
    return `${name}={${value.source}}`;
  }
  if (value === null) return `${name}={null}`;
  if (typeof value === "string") return `${name}=${JSON.stringify(value)}`;
  if (typeof value === "number" || typeof value === "boolean") {
    return `${name}={${JSON.stringify(value)}}`;
  }
  // Object or array — stringify as-is. JSX expression slot accepts any
  // valid JS expression; JSON is a valid JS literal for primitives + objects.
  return `${name}={${JSON.stringify(value)}}`;
}

function isUnserializable(v: unknown): v is UnserializableRef {
  return (
    typeof v === "object" &&
    v !== null &&
    "__unserializable" in v &&
    (v as { __unserializable: unknown }).__unserializable === true
  );
}

/**
 * Produces an ES-module-valid specifier (forward slashes, drops the .tsx/.ts
 * extension — rolldown resolves both forms).
 *
 * On Windows, `path.relative()` cannot express a cross-drive path as a
 * relative specifier (e.g. from C:\Temp to F:\project). In that case we fall
 * back to an absolute POSIX-style specifier so rolldown can still resolve the
 * file by its on-disk location.
 */
function toImportSpecifier(fromDir: string, toFile: string): string {
  const rel = path.relative(fromDir, toFile);
  // path.relative returns the absolute toFile unchanged when it cannot be
  // expressed as a relative path (cross-drive on Windows). Detect this by
  // checking whether the result is still absolute.
  if (path.isAbsolute(rel)) {
    // Use the absolute path directly, normalised to forward slashes.
    return rel.replace(/\\/g, "/").replace(/\.tsx?$/, "");
  }
  const noExt = rel.replace(/\\/g, "/").replace(/\.tsx?$/, "");
  return noExt.startsWith(".") ? noExt : `./${noExt}`;
}
